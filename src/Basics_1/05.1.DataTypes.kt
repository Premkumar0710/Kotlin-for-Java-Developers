package `01_Basics/`

fun main() {

    // In Kotlin we don't have any concepts of primitives. Here everything are pretty much objects

    var num : Int = 10
    var bigNum : Long = 100L
    var decimalNum : Double = 0.01
    var bigDecimalNum : Float = 3.14F
    var boolean : Boolean = false
    var s : String = "Hello all !!"
    var c : Char = 'A'

/*   We have another datatype called Any. All the datatypes comes from Any
     Typically we would be able to use any datatypes with Any
*/
    var anyUsage : Any = 10

}